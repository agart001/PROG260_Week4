namespace PROG260_Week4.Utility
{
    /// <summary>
    /// Utility class for console-related operations, providing methods to interact with the console output.
    /// </summary>
    public static class U_Console
    {
        /// <summary>
        /// Prints the specified string to the console followed by a new line.
        /// </summary>
        /// <param name="input">The string to be printed to the console.</param>
        public static void Print(string input) => Console.WriteLine(input);
    }
}